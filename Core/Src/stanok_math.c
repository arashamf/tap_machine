void calc_period_pulse (turn_data_t * handle)
{
	handle->PulseInSecond = ((handle->TurnInMinute)*STEPS_IN_REV)/(SECOND_PER_MINUTE); //частота сигнала
	handle->PulsePeriod = 1000000/handle->PulseInSecond;	//период сигнала в мкс
}

